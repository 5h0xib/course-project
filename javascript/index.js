

// toggle function to display and hide the display
var x = document.getElementById(".login-pop");
var isVisible = true;

 function toggle() {
  if (isVisible) {
    x.style.display = "none";
    isVisible = false;
  } else {
    x.style.display = "block";
    isVisible = true;
  }
}

